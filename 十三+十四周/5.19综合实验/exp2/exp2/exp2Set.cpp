void Cexp2Set::DoFieldExchange(CFieldExchange* pFX)
{
	pFX->SetFieldType(CFieldExchange::outputColumn);
// RFX_Text() 和 RFX_Int() 这类宏依赖的是
// 成员变量的类型，而不是数据库字段的类型。
// ODBC 尝试自动将列值转换为所请求的类型
	RFX_Text(pFX, _T("[姓名]"), column1);
	RFX_Text(pFX, _T("[学号]"), column2);
	RFX_Text(pFX, _T("[路径]"), column3);
	RFX_Long(pFX, _T("[年龄]"), column4);
	RFX_Long(pFX, _T("[薪水]"), column5);
	RFX_Text(pFX, _T("[专业]"), column6);
	RFX_Text(pFX, _T("[性别]"), column7);
	RFX_Text(pFX, _T("[生日]"), column8);
	RFX_Text(pFX, _T("[手机号]"), column9);
	RFX_Text(pFX, _T("[住址]"), column10);
}
